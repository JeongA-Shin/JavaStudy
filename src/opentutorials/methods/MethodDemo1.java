package opentutorials.methods;

public class MethodDemo1 {

    //함수
    public static void numbering(){
        int i=0;
        while(i<10){
            System.out.println(i);
            i++;
        }
    }

    //메인함수
    public static void main(String[] args){
        numbering(); //함수를 호출함
    }



}



